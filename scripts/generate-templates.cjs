#!/usr/bin/env node
/**
 * Reads room JSON files from lib/game/rooms/custom/ and generates
 * lib/game/rooms/templates.ts with all templates.
 *
 * Usage:
 *   node scripts/generate-templates.cjs
 *   npm run gen-templates
 *
 * Editor workflow:
 *   1. Open room-editor.html
 *   2. Design room → Export → Download JSON
 *   3. Save file to lib/game/rooms/custom/<id>.json
 *   4. Run: npm run gen-templates
 *   5. Restart dev server
 */

const fs = require("fs");
const path = require("path");

const CUSTOM_DIR = path.join(__dirname, "..", "lib", "game", "rooms", "custom");
const OUT_FILE = path.join(__dirname, "..", "lib", "game", "rooms", "templates.ts");

function loadCustomTemplates() {
  if (!fs.existsSync(CUSTOM_DIR)) return [];
  const files = fs.readdirSync(CUSTOM_DIR).filter((f) => f.endsWith(".json"));
  const templates = [];

  for (const file of files) {
    try {
      const raw = fs.readFileSync(path.join(CUSTOM_DIR, file), "utf8");
      const data = JSON.parse(raw);
      const items = Array.isArray(data) ? data : [data];

      for (const item of items) {
        if (!item.id) {
          console.warn(`warn: ${file}: missing 'id', skipping`);
          continue;
        }
        if (!Array.isArray(item.obstacles) && !Array.isArray(item.hazards)) {
          console.warn(`warn: ${file}: invalid template shape, skipping`);
          continue;
        }
        templates.push({
          id: item.id,
          obstacles: item.obstacles || [],
          hazards: item.hazards || [],
          needDoors: item.needDoors,
        });
      }
    } catch (err) {
      console.warn(`warn: ${file}: ${err.message}, skipping`);
    }
  }

  return templates;
}

function fmtRect(r) {
  return `{ x: ${r.x}, y: ${r.y}, w: ${r.w}, h: ${r.h} }`;
}

function fmtTemplate(t) {
  const obs = t.obstacles.length
    ? "\n" + t.obstacles.map((r) => `      ${fmtRect(r)},`).join("\n") + "\n    "
    : "";
  const haz = t.hazards.length
    ? "\n" + t.hazards.map((r) => `      ${fmtRect(r)},`).join("\n") + "\n    "
    : "";

  return `  {
    id: "${t.id}",
    obstacles: [${obs}],
    hazards: [${haz}],
  }`;
}

function generateSource(templates) {
  return `import type { RoomTemplate } from "./types";

// Auto-generated by scripts/generate-templates.cjs — do not edit manually.
// To add a room: save JSON to lib/game/rooms/custom/<id>.json, then run: npm run gen-templates
export const ROOM_TEMPLATES: RoomTemplate[] = [
${templates.map(fmtTemplate).join(",\n")},
];
`;
}

// ---- main ----
const templates = loadCustomTemplates();

if (templates.length === 0) {
  console.log("No custom templates found in lib/game/rooms/custom/");
  console.log("Save room JSON files there and re-run.");
  process.exit(0);
}

// Deduplicate by id (last file wins)
const seen = new Map();
for (const t of templates) {
  if (seen.has(t.id)) {
    console.warn(`warn: duplicate id "${t.id}" — last file wins`);
  }
  seen.set(t.id, t);
}

const unique = [...seen.values()];
const source = generateSource(unique);

fs.writeFileSync(OUT_FILE, source, "utf8");
console.log(`Generated templates.ts with ${unique.length} template(s):`);
for (const t of unique) {
  console.log(`  ${t.id} — ${t.obstacles.length} obstacles, ${t.hazards.length} hazards`);
}
